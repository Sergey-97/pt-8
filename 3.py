# 3. Известно, что рост футболистов в сборной распределен нормально с дисперсией генеральной совокупности,
# равной 25 кв.см. Объем выборки равен 27, среднее выборочное составляет 174.2.
# Найдите доверительный интервал для математического ожидания с надежностью 0.95.

import numpy as np
from scipy.stats import t

# Значения роста футболистов выборки
heights = np.array([174.2] * 27)

# Размер выборки
n = len(heights)

# Среднее значение выборки
mean = np.mean(heights)

# Стандартное отклонение выборки
std = np.sqrt(25)

# Критическое значение t-распределения для надежности 0.95 и степеней свободы (n-1)
t_value = t.ppf(0.975, n-1)

# Стандартная ошибка среднего
se = std / np.sqrt(n)

# Доверительный интервал
ci = (mean - t_value * se, mean + t_value * se)

print("Доверительный интервал:", ci)
